#include "fractol.h"

int	ft_isdouble(char *s)
{
	char	p;

	p = 0;
	if (*s == '+' || *s == '-')
		s++;
	if (*s == '.')
		return (0);
	while (s && *s)
	{
		if (!ft_isdigit(*s) && *s != '.')
			return (0);
		else if (*s == '.' && ++p)
			if (p == 2)
				return (0);
		s++;
	}
	return (1);
}
